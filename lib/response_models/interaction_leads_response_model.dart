class InteractionLeadsResponseModel {
  InteractionLeadsResponseModel({
    required this.id,
    required this.interactionDate,
    required this.previousInteractionDate,
    required this.previousInteractionTime,
    required this.notes,
    required this.lead,
    required this.createdBy,
  });

  final int? id;
  final DateTime? interactionDate;
  final DateTime? previousInteractionDate;
  final String? previousInteractionTime;
  final String? notes;
  final int? lead;
  final CreatedBy? createdBy;

  factory InteractionLeadsResponseModel.fromJson(Map<String, dynamic> json){
    return InteractionLeadsResponseModel(
      id: json["id"],
      interactionDate: DateTime.tryParse(json["interaction_date"] ?? ""),
      previousInteractionDate: DateTime.tryParse(json["previous_interaction_date"] ?? ""),
      previousInteractionTime: json["previous_interaction_time"],
      notes: json["notes"],
      lead: json["lead"],
      createdBy: json["created_by"] == null ? null : CreatedBy.fromJson(json["created_by"]),
    );
  }

}

